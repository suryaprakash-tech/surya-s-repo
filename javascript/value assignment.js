//var 
var a=30;
console.log(a);
a=56;
console.log(a);  //we can reassign the values in 'var'.


//let 

let b= 22;
console.log(b);
b=33;
console.log(b);  //here 'let' also allows us to reassign the values


//const

const c=23;
console.log(c);
c=43;
console.log(c);  //here in 'const' we cant reassign the values  


